
public class for_each_loop {
	public static void main(String args[]) {
		//Problem01
		String[] cars = {"Volvo", "BMW", "Ford", "Mazda"};
		for(String car : cars) {
			System.out.println(car);
		}
		System.out.println();
		
		//Problem02
		int[] numbers = {10, 20, 30, 40};

		for (int num : numbers) {
		  System.out.println(num);
		}
	}

}
